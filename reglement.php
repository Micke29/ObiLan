		<H5>Déroulement du tournoi :</H5>

		<p>Le tournoi: La compétition, se joue en tournoi à double élimination (dit «double bracket»). A chaque stade de la compétition, chaque joueur affrontera un seul autre joueur lors d’une rencontre (ci-après la «rencontre»). Une rencontre est composée de plusieurs matchs où les joueurs s’affrontent durant des rounds (2 rounds gagnants par match).</p>

		<p>Victoire et défaite: Pour gagner une rencontre, un joueur doit être le premier des deux joueurs à gagner deux matchs; à l’exception des quatre dernières rencontres où pour gagner une rencontre, un joueur doit être le premier des deux joueurs à gagner trois matchs. Selon les termes consacrés, seule la finale winner bracket, finale loser bracket, grande finale loser bracket, grande finale seront jouées en trois matchs gagnants.</p>

		<p>À l’issue de chaque rencontre, le joueur vainqueur reste dans l’arbre de tournoi vainqueur (ci-après le «winner bracket»); le joueur perdant passe dans l’arbre de tournoi perdant (ci-après le «loser bracket»). Quand un joueur perd le loser bracket, il est définitivement éliminé de la compétition.</p>

		<p>Devoir de ponctualité: Il appartient au joueur de se présenter à temps au lancement de son match. Si, malgré plusieurs appels, un joueur ne se présente pas pour son match à l’heure annoncée, l’autre joueur de la rencontre est considéré vainqueur par forfait de la rencontre. Dans le cas d’une rencontre du winner bracket, le joueur est envoyé en loser bracket. Dans le cas d’une rencontre de la loser bracket, il est éliminé de la compétition.</p>

		<p>Devoir de comportement: les joueurs doivent avoir un comportement exemplaire lors de l’évènement, en particulier durant les phases de la compétition. Retenir, pousser, frapper les autres concurrents ou toutes autres formes d’entrave volontaire est passible de disqualification. Le directeur de la compétition peut disqualifier un joueur à sa seule discrétion. La décision du directeur du tournoi est définitive et sans appel.</p>
