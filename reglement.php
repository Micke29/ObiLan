			<H3> 1. Règlement </H3>

		<H5>1.1. Application du règlement</H5>

		<p>Ce règlement fait office de document de référence durant l’entièreté du tournoi OBILAN League of Legends ayant lieu du 27 au 28 septembre 2019.</p>

		<p>Tout manquement au respect de ce règlement entraînera des sanctions immédiates et irrévocables. De plus, les sanctions sont collectives et tout participant sanctionné, sanctionnera inéluctablement son équipe.</p>

		<H5>1.2. Destinataires</H5>

		<p>Ce règlement s’applique à toutes les équipes participant au tournoi OBILAN  League of Legends. Sont également impliquées dans leur intégralité, les structures et équipes, ceci incluant notamment les coaches, managers, propriétaires, membres, employés et bénévoles. L’ensemble de ces personnes obtiennent la dénomination de “participants” dans la suite de ce document.

		<H5>1.3. Modifications du règlement</H5>

		<p>Seuls les organisateurs et équipes en charge du tournoi, nommés administrateurs ou "admins" dans la suite de ce document, se réservent le droit de modifier le règlement en tout

		temps et sans préavis, cela dans le plus grand respect de l'équité et de l'impartialité envers les participants de ce tournoi.</p>

		<H5>1.4. Finalité des décisions</H5>

		<p>Les admins se réservent le droit de prendre des décisions en tout temps sur tous les points non explicités dans le présent document. Ainsi, ils peuvent également déroger au règlement lorsqu’ils le jugent nécessaire. Ces décisions, une fois officialisées, sont finales et sans appel.</p>

		

			<H3> 2. Teams + Staff </H3>

		<H5>2.1. Roster</H5>

		<p>Un roster est obligatoirement composé de 5 joueurs Ce roster ne pourra jamais être modifié au cours d’un même match du tournoi et doit rester le même du début à la fin de ce même match, sous peine de sanctions. Sauf cas exceptionnel, dans lequel l’administrateur responsable du tournoi prendra la décision en accord avec le responsable général des tournois, aucun remplaçant hormis ceux inscrits n’est accepté lors du tournoi.</p>

		<H5>2.2. Les Joueurs</H5>

		<p>Les joueurs sont les membres des équipes ou structures prenant part aux parties compétitives du tournoi. En prenant part au tournoi, tout participant s’engage à avoir pris connaissance du contenu du règlement et à le respecter dans son intégralité. Tout manquement au respect de ce règlement entraînera des sanctions immédiates et irrévocables. De plus, les sanctions sont collectives et tout participant sanctionné, sanctionnera inéluctablement son équipe.</p>

		<H5>2.3. Capitaine</H5>

		<p>Le capitaine d’équipe est le joueur en charge de son équipe auprès des administrateurs du tournoi. Le capitaine d’équipe doit rester la même personne durant l'intégralité du tournoi. Il doit s’occuper du processus de sélection du côté de l’équipe, des plaintes officielles, ou de tout autre problème auprès des administrateurs du tournoi. En cas de problème, le capitaine est la seule personne autorisée à se rendre sur Discord ou au stand admin pour s’entretenir avec les admins. Les capitaines sont responsables du relai de l’information entre les admins et leur équipe, de la création des parties, du report du score ainsi que du bon déroulement du tournoi. Un capitaine d’équipe est un élément au comportement et à la communication exemplaire.</p>

		<p>Le capitaine se doit d’avoir pris connaissance du présent règlement, de s’être inscrit et d’utiliser les outils nécessaires au déroulement du tournoi, à savoir : Discord et le client League of Legends
		<H5>2.4. Les administrateurs</H5>

		<p>L’administrateur en charge de votre match sera disponible pour toute question ou demande au stand admin de la LAN. Les administrateurs ou "admins" sont les garants du bon fonctionnement de l'intégralité du tournoi. Ils sont les gérants des aspects organisationnels et arbitraux du tournoi. Ils sont les seuls à pouvoir départager les parties en cas de litige et leurs décisions sont incontestables.De plus, une fois une décision prise elle s'appliquera à tous les cas similaires subséquents. Toutes remises en question abusives et injustifiées, des choix et consignes, des administrateurs pourront être sanctionnées.</p>

		<H3>3. Structure du tournoi</H3>

		<H5>3.1. Début et fin du tournoi</H5>

		<p>Accueil des joueurs : Vendredi 27 Septembre - 19h00</p>

		<p>Début  du tournoi : Vendredi 27 Septembre - 21h (sous réserve qu’aucun problème technique n’ait lieu)</p>

		<p>Interruption du tournoi : Samedi 28 Septembre - 1h00</p>

		<p>Reprise du tournoi : Samedi 28 Septembre - 10h00</p>

		<p>Fin de tournoi : 19h au plus tard</p>

		<H5>3.2. Format du tournoi</H5>

		<p>Le tournoi est composé de 2 phases distinctes :</p>

		<p>- Une phase de poule où chaque équipe affrontera 3 autres équipes, une seule fois, dans un BO1. Chaque équipe sera qualifiée dans l’arbre final dans tous les cas, les 2 meilleures de chaque groupe iront directement en winner bracket, tandis que les 2 autres commenceront directement en looserbracket</p>

		<p>- Un arbre final à double élimination, en BO3 côté winner, en BO1 côté looser.</p>

		

		<p>Le format BO1 changera pour les derniers matchs du looser bracket. Un BO5 peut être envisagé en grande finale avec une manche d’avance pour l’équipe du winner bracket.</p>

		<H5>3.3. Paramètres de partie</H5>

		<p>Faille de l’invocateur</p>

		<p>Mode Draft de Tournoi</p>

		<H5>3.4. Spectateurs</H5>

		<p>Seuls les admins tournoi et les streamers/enregistreurs (autorisés sur demande AVANT le début du tournoi) seront autorisés à assister en tant que spectateur d’une partie. Toute présence tierce aux joueurs des équipes concernées par le match joué,au staff d’organisation et au streamers/enregistreurs agréés du tournoi est formellement interdite.</p>

		

		<H3>4. Paramètres</H3>

		<H5>4.1. Version du jeu</H5>

		<p>La compétition utilise la dernière version de League of Legends. Tous les joueurs doivent s’assurer que leur jeu est à jour. Le dernier patch doit être téléchargé au plus tard avant le premier match ou pendant le tournoi (si un nouveau patch est disponible). Sauf cas exceptionnel autorisé par les administrateurs, tout abus fait encourir un risque de disqualification.</p>

		

		<p>En cas de sortie d’un nouveau champion ou un d’important rework moins d’une semaine avant le tournoi, il sera décidé sur place, avec les capitaines de l’autorisation du champion pendant l’événement.</p>

		<H5>4.2. Mode de jeu</H5>

		<p>Chaque partie est jouée en mode “Draft de Tournoi”.</p>

		<H5>4.3. Paramètres du joueur</H5>

		<p>-Les joueurs doivent utiliser que le compte indiqué lors de l’inscription. Aucun changement n’est autorisé, sous peine de disqualification de l'équipe.</p>

		<p>-Il est interdit d’utiliser un ou des programmes modifiant la configuration standard du jeu ou d’utiliser des fichiers modifiés du client (par exemple, les modes, même purement

		graphiques ou purement audio sont interdits).</p>

		<p>-Tout type d’outil qui affiche des informations du jeu ou du système pendant le jeu (exemple Nvidia SLI Display, Rivaturner Overlays, etc.) est strictement interdit.</p>

		<p>-Les logiciels d’enregistrement audio ou visuel (sans donner d’information « in-game » sont autorisés et même fortement recommandés car utiles lors de litiges)</p>

		<p>-Les logiciels de communications VoIP sont autorisés pour la communication entre les joueurs d’un même roster uniquement.</p>

		<p>-Tout manquement aux dispositions techniques mentionnées ci-dessus, sera sanctionné immédiatement.</p>

		<p>-L’utilisation de site tiers comme OP.GG est autorisé.</p>

		

		<H3>5. Déroulement des parties</H3>

		<H5>5.1. Installation + Préparation matérielle et logicielle</H5>

		<p>Les participants sont responsables de la bonne installation de leur matériel et de son fonctionnement, incluant câbles et adaptateurs adéquats. Les administrateurs du tournoi ne sont pas autorisés à venir en aide aux participants s’ils rencontrent des problèmes techniques. Une équipe peut être sanctionnée, voir éliminée, si des problèmes matériels trop fréquents empêchent le bon déroulement du tournoi. Le matériel des joueurs doit être prêt à l’utilisation dès 10h30 le samedi matin</p>

		<H5>5.2. Avant la partie</H5>

		<p>Au début de chaque ronde, le capitaine d’équipe étant dans l’équipe du haut crée un lobby dans League of Legends et y invite le capitaine adverse. Chaque capitaine invite ensuite l’intégralité

		de son équipe.</p>

		<H5>5.3. Début de partie</H5>

		<p>Les participants doivent commencer leur match au plus tard à l’heure prévue et selon les règles décrites dans la marche à suivre de création de partie. Si les participants de chaque

		côté sont prêts avant l’heure de convocation, ils ont l’autorisation de commencer leurs matchs avant l’heure prévue, à la condition qu’ils effectuent tous leurs matchs dans l’ordre

		prévu selon les pools Challonge. Si des joueurs sont en retard, il est demandé à la team adverse de les attendre 15 minutes. Ensuite, et ce quoi qu’il arrive, le match doit être lancé.</p>

		<H5>5.4. Arrêt volontaire du match en cours</H5>

		<p>L'arrêt volontaire, hors abandon d’une équipe, d’un match est interdit et entraîne des

		sanctions ou une disqualification.</p>

		<H5>5.5. Interruption involontaire d’un match</H5>

		<p>Si un match est involontairement interrompu (plantage, déconnexion réseau, etc.) les règles suivantes doivent être suivies :</p>

		<p>- Si le problème a lieu pendant les deux premières minutes et que les tentatives de reconnexion multiples restent infructueuses, alors un admin doit immédiatement être averti par le biais des deux capitaines d’équipe (et seulement eux), le match sera alors considéré comme nul, et pourra être relancé avec les mêmes picks et bans.</p>

		<p>- Si le problème a lieu après les deux minutes, le joueur ayant planté ou ayant eu une déconnexion a 15 minutes pour tenter de se reconnecter. S’il y a trop de tentatives infructueuses, un admin doit immédiatement être averti par le capitaine du joueur en difficulté. En parallèle, l’équipe en sous-nombre peut décider de poursuivre le match en sous-nombre où d’abandonner, entraînant ainsi une défaite.

		Cependant, les solutions favorisant le fair-play étant privilégiées, le match pourra éventuellement être rejoué avec l’accord de l’équipe adverse. Aucun arrangement à l’amiable n’est autorisé sans l’accord d’un Administrateur. Ces derniers pourront, en cas de validation, veiller à ce que les conditions soient respectées.</p>

		<p>Tous les arrangements faits à l’amiable sans validation officielle seront ignorés par les officiels et considérés comme nuls et non-avenus en cas de litige.</p>

		<H5>5.7. Validation du score</H5>

		<p>A la fin d’un match, les capitaines d’équipes doivent avertir les administrateur League of Legends</p>

		<p>Toutes autres preuves numériques pouvant être considérés comme irréfutables par les admins (photos, vidéos, etc.) peuvent être prise en considération.</p>

		<p>Une fois que le résultat aura été reporté, contrôlé et validé par un administrateur, il sera définitif et l’équipe ne pourra plus faire appel de cette décision.</p>

		<p>Tout manquement ou oubli est passible de sanctions ou disqualifications.</p>

		<H5>5.8. Demande d’investigation</H5>

		<p>Lorsqu’une équipe s’estime victime d’une violation du règlement durant son match, les joueurs doivent immédiatement le signaler à la fin du match. Le capitaine de chaque équipe

		et uniquement ces personnes-là devront contacter les administrateurs pour demander une investigation et en expliquer la raison. Il est impératif de fournir des informations valables et pertinentes qui aideront les officiels du tournoi dans leur investigation. Les demandes d’investigation injustifiées et répétées seront sévèrement sanctionnées.</p>

		

		<H3>6. Infractions au règlement</H3>

		<H5>6.1. Installation de logiciel interdit</H5>

		<p>Les joueurs ne peuvent pas amener ou utiliser de logiciel externe qui interagit avec le jeu, ou toute forme de modification qui altère la version du jeu vidéo ou permet à un joueur

		d’exécuter plus d’une action en même temps, ou tout fichier de configuration.</p>

		<H5>6.2. Actions et comportements interdits dans le jeu entraînant des sanctions</H5>

		<p>Les actions suivantes sont strictement interdites durant un match et entraîneront une défaite immédiate et irrévocable du match en cours :</p>

		<p>- Toute utilisation de script</p>

		<p>- L’utilisation d’un bug ou exploit clairement injuste</p>

		<p>- Contester avec véhémence la décision d’un Administrateur du tournoi</p>

		<p>- Toute action jugée agressive et/ou déplacée envers un/des administrateurs,un des participants ou le public présent sera immédiatement sanctionnée.</p>

		

		<p>Une équipe sera immédiatement disqualifiée du tournoi si l’un de ses joueurs commet l’une des infractions suivantes :</p>

			<p>- L’utilisation de n’importe quel programme de triche</p>

			<p>- Avoir un comportement clairement inapproprié</p>

			<p>- Utiliser un langage ou des gestes insultants</p>

			<p>- Émettre des insultes, propos négatifs ou railleries à répétition. Ceci inclut les railleries en partie (« flaming »).</p>

			<p>- Être coupable de comportement anti-sportif (déconnection intentionnelle, spam/flood, etc.)</p>

			<p>- Tricher avec préméditation ou utiliser un procédé inéquitable (ceci inclut également la réception d’informations non autorisées sur la partie en cours)</p>

			<p>- Mentir ou induire en erreur un Administrateur du tournoi</p>

			<p>- Refuser à répétition de suivre les instructions d’un Administrateur du tournoi</p>

			<p>- Violer intentionnellement et consciemment des règles mentionnées dans ce règlement</p>

		<H5>6.3. Disqualification et expulsion</H5>

		<p>Après étude par un admin d’une infraction au règlement, une équipe ayant reçu une sanction peut également subir plusieurs défaites automatiques, la disqualification de l’équipe ou

		même l’expulsion de l’équipe. Une équipe disqualifiée perd automatiquement tous ses prochains matchs et perd tous ses droits et dotations et prix qu’elle aurait pu recevoir. Dans

		une phase de poules, tous les résultats des matchs précédents sont convertis en défaite. Dans une phase finale, le match précédent est converti en défaite et son adversaire précédent avance au tour suivant à la place de l’équipe éliminée.</p>

		

