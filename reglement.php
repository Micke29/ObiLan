			Pour le premier tour, les matchs sont définis aléatoirement entre les douze équipes.<br>
			A la fin de ce tour, 6 équipes auront un statut de 1-0 (1 victoire et 0 défaite) et 6 équipes un statut de 0-1.
		</p>

		<p>
			Pour le deuxième tour, les matchs sont définis aléatoirement entre les équipes qui ont le même statut.<br>
			Il y aura donc 3 matchs "1-0" et 3 matchs "0-1". A la fin de ce tour, trois équipes seront en 2-0 et auront fini la ronde suisse en ayant un seed 1, six équipes seront en 1-1 et trois équipes seront en 0-2 et auront fini la ronde suisse avec un seed 3.
		</p>

		<p>
			Pour le troisième tour, les matchs sont définis aléatoirement entre les équipes en 1-1. Deux équipes ne peuvent pas s'affronter une deuxième fois.<br>
			A la fin de ce tour, trois équipes seront en 2-1, celle avec la meilleure différence de rounds* sera en seed 1 et les deux autres en seed 2.<br>
			A la fin de ce tour, il y aura trois équipes en 1-2, celle avec la moins bonne différence de rounds* sera en seed 3 et les deux autres en seed 2.
		</p>

		<p>
			* : La différence de round se fait entre le nombre de rounds remportés et le nombre de rounds perdus.<br>
