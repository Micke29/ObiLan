			<span>Mode de jeu</span><br>
			Les matchs devront se faire en partie draft de tournoi, faille de l'invocateur.<br>
			Les Bans et Picks devront être réalisés avant le début de la partie et doivent respecter la procédure du draft :<br>
			- L'équipe A retire l'un des champions disponibles<br>
			- L'équipe B retire un deuxième des champions disponibles<br>
			- L'équipe A retire un troisième des champions disponibles<br>
			- L'équipe B retire un quatrième des champions disponibles<br>
			- L'équipe A retire un cinquième des champions disponibles<br>
			- L'équipe B retire un sixième des champions disponibles<br>
			- L'équipe A choisit l'un des champions parmi ceux encore disponibles<br>
			- L'équipe B choisit deux champions<br>
			- L'équipe A choisit deux champions<br>
			- L'équipe B choisit deux champions<br>
			- L'équipe A choisit deux champions<br>
			- L'équipe B choisit un champion
		</p>

		<p>
			<span>Classement lors des phases de poules</span><br>
			Les critères suivant sont appliqués pour le classement dans une poule :<br>
			- Plus grand nombre de points.<br>
			- Si égalité entre deux équipes, le vainqueur du match les ayant opposés prend l'avantage.<br>
			- Si égalité entre plus de deux équipes, une équipe ayant un indice de temps global plus élevé sera classé devant une équipe ayant un indice de temps global plus faible.<br>
			- Un indice de temps représente la valeur inverse de la durée d'une rencontre (arrondie à la minute). L'indice de temps global d'une équipe est la somme des indices de temps global, tandis que l'indice de temps d'une victoire sera ajouté à votre indice de temps global.<br>
			- En dernier recours, une décision sera prise par les administrateurs.
