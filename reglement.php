			- Le « mapswimming » ou « floating » (c.f nager dans les airs) est interdit.<br>
			- Le « Pixelwalking (c.f marcher ou rester sur des éléments invisibles de la map) est interdit.<br>
			- Utiliser “+duck“ sur le bouton du milieu de la souris.<br>
