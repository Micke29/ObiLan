			- Refuser de suivre les instructions d’un officiel du tournoi ;<br>
			- Arriver en retard à l’heure de convocation ;<br>
			- Contester la décision d’un officiel de tournoi ;<br>
			- Utiliser un langage ou des gestes insultants ;<br>
			- Être coupable de comportement antisportif ;<br>
			- Recevoir plus d’un avertissement ;<br>
			- Être coupable d’actes violents ;<br>
			- Tricher ou utiliser un procédé inéquitable ;<br>
			- Mentir ou induire en erreur un officiel du tournoi ;<br>
			- Violer les règles de ce règlement.<br>
