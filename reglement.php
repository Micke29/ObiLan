			- L’utilisation de la pause en cours de round est interdite (excepté à la fin d’un round et pendant la période de freeze).<br>
			- Toute forme d’utilisation de script est interdite.<br>
			- Utiliser un bug qui change le principe du jeu (c.f bugs de spawns) est interdit.<br>
			- Se déplacer à travers les murs, les plafonds ou sols est interdit. Cela inclut le fait de marcher dans le ciel.<br>
