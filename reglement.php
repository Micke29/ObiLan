		<span>ARTICLE 1 - PHASE DE POULE</span><br>
		<p>	La phase de poule se déroule selon un format de "Ronde Suisse" pour définir les seeds de l'arbre, il y a trois seeds différents : seed 1, seed 2 et seed 3, le seed 1 étant plus avantageux que les autres pour la position dans l'arbre.<br> 
			Aucune équipe ne sera éliminée à la fin de la ronde suisse.
		</p>

		<p>
			Les participants se rencontrent tous dans un « Best-of 1 » (BO1).<br>
			La ronde suisse s'effectue en trois tours de matchs.
		</p>

		<p>
			Pour le premier tour, les matchs sont définis aléatoirement entre les douze équipes.<br>
			A la fin de ce tour, 6 équipes auront un statut de 1-0 (1 victoire et 0 défaite) et 6 équipes un statut de 0-1.
		</p>

		<p>
			Pour le deuxième tour, les matchs sont définis aléatoirement entre les équipes qui ont le même statut.<br>
			Il y aura donc 3 matchs "1-0" et 3 matchs "0-1". A la fin de ce tour, trois équipes seront en 2-0 et auront fini la ronde suisse en ayant un seed 1, six équipes seront en 1-1 et trois équipes seront en 0-2 et auront fini la ronde suisse avec un seed 3.
		</p>

		<p>
			Pour le troisième tour, les matchs sont définis aléatoirement entre les équipes en 1-1. Deux équipes ne peuvent pas s'affronter une deuxième fois.<br>
			A la fin de ce tour, trois équipes seront en 2-1, celle avec la meilleure différence de rounds* sera en seed 1 et les deux autres en seed 2.<br>
			A la fin de ce tour, il y aura trois équipes en 1-2, celle avec la moins bonne différence de rounds* sera en seed 3 et les deux autres en seed 2.
		</p>

		<p>
			* : La différence de round se fait entre le nombre de rounds remportés et le nombre de rounds perdus.<br>
			S'il y a encore égalité, les équipes seront départagées sur leur confrontation directe. S'il n'y a pas eu de confrontation directe, les équipes seront départagées avec un "pile ou face" en une manche.
		</p>

		<p>
			<span>ARTICLE 1.2 - PHASE FINALE</span><br>
			La phase finale se déroule selon un arbre à double élimination.<br>
			Les équipes sont placées dans l'arbre grâce à leur seed obtenu lors de la phase de poule.
		</p>

		<p>
			Les équipes seed 2 affrontent les équipes seed 3 en huitième de finale du "winner bracket".<br>
			Les équipes seed 1 sont directement placées en quart de finale du "winner bracket".
		</p>

		<p>
			Une défaite en "winner bracket" vous place en "loser bracket". Une défaite en "loser bracket" vous élimine du tournoi.<br>
			Les matchs de winner bracket seront joués en "BO3"** et les matchs du loser bracket seront joués en "BO1"
		</p>

		<p>
			**Selon l'avancement du tournoi, les admins peuvent reprogrammer des matchs "BO3" en matchs "BO1".
		</p>
		<p>
			<span>ARTICLE 2.1 - MATCH "BEST OF 1"</span><br>
			Un match « Best of 1 » se joue en une partie et oppose deux équipes de 5 joueurs. Au début de la map, un kniferound sera joué.<br>
			Le vainqueur du kniferound choisi dans quel camp il commence le match. Une partie est jouée sur une map de 2 manches de 15 rounds.<br>
			Les équipes doivent jouer alternativement terroriste et contre-terroriste en changeant de manche, et marquent un point en gagnant un round dans le jeu. Le gagnant du match est l’équipe qui a gagné 16 rounds.<br>
			Lorsqu’une équipe gagne 16 rounds, le match se termine et les rounds restants ne sont pas joués. En cas d'égalité entre les deux équipes lors de la phase de poule, les équipes doivent se départager lors d'un OVERTIME.
		</p>
		<p>
			<span>ARTICLE 2.2 - MATCH "BEST OF 3"</span><br>
			Un « Best of 3 » est joué en deux maps et oppose deux équipes de 5 joueurs.<br>
			Chaque map se joue en deux manches de 15 rounds.<br>
			Au début de chaque map, un kniferound sera joué. Le vainqueur du kniferound choisi dans quel camp il commence la map.<br>
			Les équipes doivent jouer alternativement terroriste et contre-terroriste en changeant de manche, et marquent un point en gagnant un round dans le jeu.
		</p>

		<p>
			Le vainqueur d’un match est l’équipe qui a remporté deux maps. Si les deux équipes ont chacune remporté une map, une troisième map sera jouée.
		</p>

		<p>
			Si les équipes sont à égalité à la fin d’une map, un OVERTIME sera joué.
		</p>

		<p>
			A la fin d’une map, les joueurs doivent annoncer le résultat aux officiels du tournoi avant de lancer la map suivante.
		</p>

		<p>
			<span>ARTICLE 2.3 - OVERTIME</span><br>
			Un overtime est la prolongation d'une carte.<br>
			Un overtime est joué en deux manches de 3 rounds avec un "startmoney" de 10000$.<br>
			Si les équipes sont encore à égalité à la fin des 6 rounds, un autre overtime sera lancé et ainsi de suite jusqu'à ce qu'une équipe marque 4 rounds lors d'un overtime.
		</p>

		<p>
			<span>ARTICLE 2.4 - MAP POOL</span><br>
			Le mappool officiel sera effectif durant la LAN. Il est composé des 7 maps suivantes :<br>
			- de_nuke<br>
			- de_inferno<br>
			- de_train<br>
			- de_cache<br>
			- de_mirage<br>
			- de_overpass<br>
			- de_cobblestone
		</p>

		<p>
			<span>ARTICLE 2.5 - CHOIX DE MAP</span><br>
			La désignation du joueur A et du joueur B se fait par tirage au sort.<br>
			Pour un match « Best of 1 » le choix de la map doit respecter la procédure suivante :<br>
			- Le joueur A retire l’une des 7 maps;<br>
			- Le joueur B retire l’une des 6 maps restantes ;<br>
			- Le joueur A retire l’une des 5 maps restantes ;<br>
			- Le joueur B retire l’une des 4 maps restantes ;<br>
			- Le joueur A retire l’une des 3 maps restantes ;<br>
			- Le joueur B retire l’une des 2 maps restantes ;<br>
			- Le match se joue sur la dernière map restante.
		</p>

		<p>
			Pour un match « Best of 3 » le choix de la map doit respecter la procédure suivante :<br>
			- Le joueur A retire l’une des 7 maps ;<br>
			- Le joueur B retire l’une des 6 maps restantes ;<br>
			- Le joueur A choisit l’une des 5 maps restantes ;<br>
			- Le joueur B choisit l’une des 4 maps restantes ;<br>
			- Le joueur A retire l’une des 3 maps restantes ;<br>
			- Le joueur B retire l’une des 2 maps restantes ;
		</p>

		<p>
			Le match se joue sur les deux maps choisies par les deux équipes et débute par la map du joueur A.<br>
			La dernière map restante sera utilisée pour départager les deux équipes si elles remportent chacune l’une des deux premières maps.
		</p>

		<p>
			<span>ARTICLE 3.1 - AVANT LE MATCH</span><br>
			L’organisateur essaiera autant que possible de donner une chance aux équipes de s’échauffer avant chaque match mais en raison des contraintes temporelles, il ne pourra garantir un temps minimum spécifique.<br>
			Le match doit démarrer au maximum à l’heure indiquée sur le planning, les joueurs doivent être prêts à démarrer au moins 15 minutes avant l’heure de match indiquée.<br>
			Si un quelconque problème survient avec le son, le réseau ou le pc, le joueur doit en avertir l’arbitre immédiatement afin de le résoudre au plus tôt avant le début du match.
		</p>

		<P>
			<span>ARTICLE 3.2 - CAPITAINE</span><br>
			Avant le lancement du premier match chaque équipe doit présenter son capitaine d’équipe aux officiels du tournoi. Il peut être l’un des 5 membres de l’équipe ou bien le manager. Le capitaine d’équipe doit rester la même personne durant tout le tournoi.<br>
			Le capitaine d’équipe est la personne en charge de son équipe auprès des officiels du tournoi. Il doit s’occuper du choix de la map et du côté de son équipe, des plaintes officielles, ou de tout autre problème auprès des officiels du tournoi.
		</P>

		<p>
			<span>ARTICLE 3.3 - INTERRUPTION DU MATCH</span><br>
			Si un match est involontairement interrompu (plantage, déconnexion réseau,...) les officiels du tournoi peuvent décider de recommencer le match en fonction des règles suivantes :<br>
			- Si une sauvegarde valide est disponible sur le serveur, le round dans laquelle le problème a eu lieu est restauré.<br>
			- Si le problème a eu lieu avant la fin du premier round et le premier kill, la mi-temps est entièrement rejouée.<br>
			- Si le problème a eu lieu pendant le premier round et que l’issue du round ne peut être déterminée ou devinée, le round n’est pas pris en compte, la mi-temps continue avec le nombre de rounds restant, et le score de la mi-temps interrompue sera ajouté à la nouvelle mi-temps.<br>
			- Si le problème a lieu après la fin du premier round, la mi-temps continue avec le nombre de rounds restant, et le score de la mi-temps interrompue sera ajouté à la nouvelle mi-temps. Le « start money » sera déterminé afin de compenser les joueurs.<br>
			- Si l’un des joueurs plante ou est déconnecté, il doit revenir se connecter aussi vite que possible.<br>
			- Les équipes peuvent mettre en pause la partie à la fin du round en cours ou durant la période de freeze afin de permettre au joueur de se connecter. Une équipe ne peut pas utiliser la pause pendant un round sauf si elle en reçoit l’autorisation par un officiel du tournoi.
		</p>

