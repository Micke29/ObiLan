			<span>ARTICLE 3.4 - TIMEOUTS</span><br>
			Lors de chaque match, les équipes sont autorisées à utiliser un unique timeout de 60 secondes par mi-temps. Cette pause stratégique sera effective durant un "freezetime".
		</p>

